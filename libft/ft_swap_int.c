#include "libft.h"

void	ft_swap_int(int *elem1, int *elem2)
{
	if (*elem1 != *elem2)
	{
		*elem1 ^= *elem2;
		*elem2 ^= *elem1;
		*elem1 ^= *elem2;
	}
}
